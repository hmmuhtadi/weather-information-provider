    synchronized public void refreshLists(ArrayList<String> alist, JList jlist, int incr)
    {
        DefaultListModel model = new DefaultListModel();
        jlist.removeAll();
        
        
        for(int i=0;i<alist.size();)
        {
            //model.add(i, alist.get(i));
            model.addElement(alist.get(i));
            i=i+incr;
        }
        
        jlist.setModel(model);
        
    }
    
