    public void setLabels()
    {
        int n = jPanel3.getComponentCount();
        System.out.println(""+n);

        //JComponent com ;
        JLabel  label;
        for(int i=0;i<sensorList.size();i++)
        {
            label = (JLabel) jPanel3.getComponent(i);
            
            label.setVisible(true);
            StringTokenizer location = new StringTokenizer(sensorPositions.get(i)," ");
            int x = Integer.parseInt(location.nextToken());
            int y = Integer.parseInt(location.nextToken());
            label.setLocation(x, y);
           
        }
        
    }
    
  
